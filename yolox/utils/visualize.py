highest_conf={}

def is_good_quality(image):
    # 대비를 기준으로 이미지 품질 평가
    gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    contrast = cv2.Laplacian(gray, cv2.CV_64F).var()
    return contrast > 100  # 임계값 100 추천

def vis(img, boxes, scores, cls_ids, vis_folder, conf=0.5, class_names=None):
    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        x0 = max(0,int(box[0]))
        y0 = max(0,int(box[1]))
        x1 = min(img.shape[1],int(box[2]))
        y1 = min(img.shape[0],int(box[3]))
        width = x1 - x0
        height = y1 - y0

        # 크기 조건
        size_condition = 40 <= width <= 150 and 40 <= height <= 150

        # 위치 조건 (우측 하단)
        location_condition = (
                x0 >= 405 and y0 >= 160 and
                x1 <= 640 and y1 <= 360
        )

        # 수화 선생님 필터링
        if size_condition and location_condition:
            continue

        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        # 객체별 최고 conf를 갱신
        if cls_id not in highest_conf or score > highest_conf[cls_id]['score']:
            # bbox에 해당하는 영역 크롭
            roi = img[y0:y1, x0:x1]
            if is_good_quality(roi):
                cropped_fname = f"detected_{cls_id}.jpg"
                cropped_path = os.path.join(vis_folder, cropped_fname)
                cv2.imwrite(cropped_path, roi)  # 이미지 덮어쓰기

                # 최고 conf 값 갱신
                highest_conf[cls_id] = {'score': score, 'path': cropped_path}

        txt_bk_color = (_COLORS[cls_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5*txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img


